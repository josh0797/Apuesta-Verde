def _f(v, default=0.0):
    """Safe float coercion used by F6A bullpen-swap logic."""
    try:
        return float(v) if v is not None else default
    except (TypeError, ValueError):
        return default


def _extract_line_number_from_market(market_text: Any) -> Optional[float]:
    """Extract a numeric line (e.g. 9.5) from a market label string."""
    if market_text is None:
        return None
    m = re.search(r"(\d+(?:\.\d+)?)", str(market_text))
    if not m:
        return None
    try:
        return float(m.group(1))
    except (TypeError, ValueError):
        return None



# ────────────────────────────────────────────────────────────────────────────
# Time-field normalization (GAP #0)
# ────────────────────────────────────────────────────────────────────────────
def normalize_mlb_time_fields(game: dict) -> dict:
    """Guarantee that an MLB game dict carries BOTH `gameDate` and
    `kickoff_iso` pointing to the same ISO timestamp.

    time_filter.py only reads `kickoff_iso`, but the MLB Stats API
    returns the timestamp in `gameDate`. Without this helper, finished
    matches can sneak through because the filter sees an empty
    `kickoff_iso` and falls back to a defensive drop — or worse, doesn't
    even know the kickoff has passed.

    Safe to call on any dict: if neither field exists, the dict is
    returned untouched and a warning is logged.
    """
    if not isinstance(game, dict):
        return game
    game_date = game.get("gameDate") or game.get("kickoff_iso")
    if game_date:
        game["gameDate"]    = game_date
        game["kickoff_iso"] = game_date
    else:
        log.warning(
            "MLB: juego %s sin gameDate/kickoff_iso; no se puede filtrar por tiempo",
            game.get("gamePk") or game.get("game_pk") or "?",
        )
    return game


# ────────────────────────────────────────────────────────────────────────────
# Pitcher confirmation
# ────────────────────────────────────────────────────────────────────────────
async def _confirm_pitchers_statsapi(db, date_str: str) -> tuple[dict, str]:
    """Use MLB Stats API schedule with probablePitcher hydration.
    Returns ({gamePk: {home_pitcher, away_pitcher, ...meta...}}, source_url) or ({}, '').
    """
    try:
        from .mlb_stats_api import get_schedule_with_probables
        games = await get_schedule_with_probables(db, date_str)
    except Exception as exc:
        log.debug("statsapi probables fetch failed: %s", exc)
        return {}, ""
    url = f"{MLB_STATSAPI_BASE}/schedule?date={date_str}&sportId=1&hydrate=probablePitcher,linescore,team,venue"
    out: dict[int, dict] = {}
    incomplete: list[dict] = []   # games dropped from `out` for missing pitchers
    for g in games:
        if not g.get("gamePk"):
            continue
        home_pid = g.get("home_probable_id")
        away_pid = g.get("away_probable_id")
        # GAP #1: ambos pitchers DEBEN estar confirmados — no analizamos
        # juegos con un solo probable.
        if not home_pid or not away_pid:
            log.info(
                "MLB: juego %s sin pitcher confirmado en StatsAPI (home=%s away=%s) — candidato a rescue",
                g.get("gamePk"), home_pid, away_pid,
            )
            # Stash the raw schedule entry so the rescue layer can try
            # external scrapers before we discard the game.
            incomplete.append(g)
            continue
        out[int(g["gamePk"])] = {
            "home_pitcher_id":   home_pid,
            "home_pitcher_name": g.get("home_probable_name"),
            "away_pitcher_id":   away_pid,
            "away_pitcher_name": g.get("away_probable_name"),
            "home_team_id":      g.get("home_team_id"),
            "away_team_id":      g.get("away_team_id"),
            "home_team_name":    g.get("home_team"),
            "away_team_name":    g.get("away_team"),
            "venue":             g.get("venue"),
            "gameDate":          g.get("gameDate"),
            "status":            g.get("status"),
            "abstractGameState": g.get("abstractGameState"),
            "_source":           "mlb_stats_api",
        }
    # GAP #FASE1 — external-source rescue for games StatsAPI couldn't
    # confirm. We do this here (inside _confirm_pitchers_statsapi) so
    # callers see a uniform `out` dict with the rescued games already
    # merged in. Each rescued entry carries an `_external_rescue` block
    # that the orchestrator will surface as editorial_context_signals.
    if incomplete:
        try:
            from .external_sources import mlb_lineup_rescue as _rescue
            rescued = await _rescue.rescue_mlb_pitchers(date_str, incomplete)
        except Exception as exc:
            log.debug("MLB external rescue failed: %s", exc)
            rescued = []
        for raw_g, r in zip(incomplete, rescued or []):
            # Only count as confirmed if BOTH pitchers are now known
            # (even if just by name — IDs may be missing on external).
            if r.get("home_pitcher_name") and r.get("away_pitcher_name"):
                out[int(raw_g["gamePk"])] = {
                    "home_pitcher_id":   raw_g.get("home_probable_id"),
                    "home_pitcher_name": r["home_pitcher_name"],
                    "away_pitcher_id":   raw_g.get("away_probable_id"),
                    "away_pitcher_name": r["away_pitcher_name"],
                    "home_team_id":      raw_g.get("home_team_id"),
                    "away_team_id":      raw_g.get("away_team_id"),
                    "home_team_name":    raw_g.get("home_team"),
                    "away_team_name":    raw_g.get("away_team"),
                    "venue":             raw_g.get("venue"),
                    "gameDate":          raw_g.get("gameDate"),
                    "status":            raw_g.get("status"),
                    "abstractGameState": raw_g.get("abstractGameState"),
                    "_source":           r.get("home_pitcher_source") or "external_rescue",
                    "_external_rescue":  r,   # carries sources_consulted + signals
                }
                log.info(
                    "MLB: juego %s rescatado vía %s (home=%s · away=%s)",
                    raw_g.get("gamePk"),
                    r.get("home_pitcher_source") or r.get("away_pitcher_source"),
                    r["home_pitcher_name"], r["away_pitcher_name"],
                )
            else:
                # Keep the rescue payload on the dropped game so the UI
                # can still render "Fuentes consultadas" for the discard.
                log.info(
                    "MLB: juego %s permanece descartado tras consultar fuentes externas (%s)",
                    raw_g.get("gamePk"),
                    ", ".join(s.get("source", "?") for s in (r.get("sources_consulted") or [])[:4]),
                )
    return out, url


async def _confirm_pitchers_mlb_com(date_str: str, expected_pairs: list[tuple[str, str]]) -> tuple[dict, str]:
    """Fallback: scrape mlb.com/probable-pitchers HTML. Returns ({key:
    {home, away}}, url) keyed by 'home_lower|away_lower'."""
    try:
        from .external_sources.base import direct_fetch
    except Exception:
        return {}, ""
    url = MLB_PROBABLE_PAGE.format(date=date_str)
    html = await direct_fetch(url, timeout_sec=10.0)
    if not html:
        return {}, url
    out: dict[str, dict] = {}
    # MLB.com puts probable pitchers inside `data-mlb-test` attributes. We
    # extract via regex on `probableStarter` JSON-ish strings without
    # parsing the whole page.
    for home, away in expected_pairs:
        # Try to find `away` and `home` team identifiers in close proximity
        # to a `probableStarter` block.
        pattern = re.compile(
            rf"({re.escape(home[:10])}.{{0,1200}})", re.IGNORECASE | re.DOTALL,
        )
        m = pattern.search(html)
        if not m:
            continue
        chunk = m.group(1)
        names = re.findall(r"probableStarter[^\"']{0,5}\"\s*:\s*\"([^\"]+)", chunk)[:2]
        if len(names) >= 2:
            key = f"{home.lower()}|{away.lower()}"
            out[key] = {
                "home_pitcher_name": names[0],
                "away_pitcher_name": names[1],
                "_source":           "mlb.com",
            }
    return out, url



"""MLB Day Orchestrator — wires every analytics block + pitcher confirmation
into a single `analyze_mlb_day(date_str, db=...)` entry point.

Pitcher confirmation
====================
The user explicitly asked: every editorial_context_signal generated by the
MLB engine must include the literal URL of the source that confirmed the
probable pitcher. We try, in this order:

  1. MLB Stats API `schedule?hydrate=probablePitcher` (authoritative).
     If a pitcher is listed → source_url is the literal API URL.
  2. mlb.com/probable-pitchers (HTML scrape, no BrightData) as backup.
  3. rotowire.com/baseball/daily-lineups.php as a third opinion.

Whichever source returned the pitcher is recorded in
`pitcher_confirmation_source_url`. The signals embed this URL so the
user can click and verify the pitcher independently.
"""
from __future__ import annotations

import asyncio
import logging
import re
from datetime import datetime, timezone
from typing import Any, Optional

from .mlb_pregame_analytics import (
    starting_pitcher_edge,
    pitcher_quality_score,
    bullpen_fatigue_score,
    offense_vs_pitcher_type,
    park_factor_analyzer,
    mlb_fragility_score,
    run_line_predictor,
    over_under_predictor,
    nrfi_yrfi_analyzer,
    mlb_alternative_rescue,
    emit_signals,
)

log = logging.getLogger("mlb_day_orchestrator")

MLB_STATSAPI_BASE = "https://statsapi.mlb.com/api/v1"
MLB_PROBABLE_PAGE = "https://www.mlb.com/probable-pitchers/{date}"   # date YYYY-MM-DD
ROTOWIRE_DAILY    = "https://www.rotowire.com/baseball/daily-lineups.php"


# ────────────────────────────────────────────────────────────────────────────
# Pitcher confirmation
# ────────────────────────────────────────────────────────────────────────────
async def _confirm_pitchers_statsapi(db, date_str: str) -> tuple[dict, str]:
    """Use MLB Stats API schedule with probablePitcher hydration.
    Returns ({gamePk: {home_pitcher, away_pitcher}}, source_url) or ({}, '').
    """
    try:
        from .mlb_stats_api import get_schedule_with_probables
        games = await get_schedule_with_probables(db, date_str)
    except Exception as exc:
        log.debug("statsapi probables fetch failed: %s", exc)
        return {}, ""
    url = f"{MLB_STATSAPI_BASE}/schedule?date={date_str}&sportId=1&hydrate=probablePitcher,linescore,team,venue"
    out: dict[int, dict] = {}
    for g in games:
        if not g.get("gamePk"):
            continue
        if g.get("home_probable_id") or g.get("away_probable_id"):
            out[int(g["gamePk"])] = {
                "home_pitcher_id":   g.get("home_probable_id"),
                "home_pitcher_name": g.get("home_probable_name"),
                "away_pitcher_id":   g.get("away_probable_id"),
                "away_pitcher_name": g.get("away_probable_name"),
                "_source":           "mlb_stats_api",
            }
    return out, url


async def _confirm_pitchers_mlb_com(date_str: str, expected_pairs: list[tuple[str, str]]) -> tuple[dict, str]:
    """Fallback: scrape mlb.com/probable-pitchers HTML. Returns ({key:
    {home, away}}, url) keyed by 'home_lower|away_lower'."""
    try:
        from .external_sources.base import direct_fetch
    except Exception:
        return {}, ""
    url = MLB_PROBABLE_PAGE.format(date=date_str)
    html = await direct_fetch(url, timeout_sec=10.0)
    if not html:
        return {}, url
    out: dict[str, dict] = {}
    # MLB.com puts probable pitchers inside `data-mlb-test` attributes. We
    # extract via regex on `probableStarter` JSON-ish strings without
    # parsing the whole page.
    for home, away in expected_pairs:
        # Try to find `away` and `home` team identifiers in close proximity
        # to a `probableStarter` block.
        pattern = re.compile(
            rf"({re.escape(home[:10])}.{{0,1200}})", re.IGNORECASE | re.DOTALL,
        )
        m = pattern.search(html)
        if not m:
            continue
        chunk = m.group(1)
        names = re.findall(r"probableStarter[^\"']{0,5}\"\s*:\s*\"([^\"]+)", chunk)[:2]
        if len(names) >= 2:
            key = f"{home.lower()}|{away.lower()}"
            out[key] = {
                "home_pitcher_name": names[0],
                "away_pitcher_name": names[1],
                "_source":           "mlb.com",
            }
    return out, url


# ────────────────────────────────────────────────────────────────────────────
# Stat enrichment per pitcher
# ────────────────────────────────────────────────────────────────────────────
async def _enrich_pitcher(db, pitcher_id: Optional[int]) -> dict:
    if not pitcher_id:
        return {}
    try:
        from .mlb_stats_api import get_pitcher_season_stats
        return await get_pitcher_season_stats(db, pitcher_id) or {}
    except Exception as exc:
        log.debug("pitcher stats fetch failed: %s", exc)
        return {}


# ────────────────────────────────────────────────────────────────────────────
# Public API
# ────────────────────────────────────────────────────────────────────────────
async def analyze_mlb_day(date_str: str, *, db: Any = None) -> dict:
    """Top-level orchestration for a full MLB day.

    Returns a dict with picks / rescued_picks / discarded_picks /
    fragility_scores / editorial_context_signals (one signal list per
    match keyed by gamePk).
    """
    start = datetime.now(timezone.utc)
    confirmed_by_pk, statsapi_url = await _confirm_pitchers_statsapi(db, date_str)

    picks: list[dict]       = []
    rescued: list[dict]     = []
    discarded: list[dict]   = []
    frag_by_pk: dict[int, dict] = {}
    signals_by_pk: dict[int, list[dict]] = {}
    pipeline_meta: dict = {
        "date":               date_str,
        "confirmed_games":    len(confirmed_by_pk),
        "statsapi_url":       statsapi_url,
        "started_at":         start.isoformat(),
    }

    if not confirmed_by_pk:
        # No probable pitchers from statsapi means nothing to score.
        pipeline_meta["abort_reason"] = "no_probable_pitchers"
        return _empty_payload(pipeline_meta)

    # Per-game analysis
    for game_pk, conf in confirmed_by_pk.items():
        try:
            h_stats = await _enrich_pitcher(db, conf.get("home_pitcher_id"))
            a_stats = await _enrich_pitcher(db, conf.get("away_pitcher_id"))
        except Exception:
            h_stats, a_stats = {}, {}

        # Skip games with too little pitcher data — per the user's rule
        # "NO analizar pitchers no confirmados / menos de 3 aperturas".
        starts_h = int((h_stats or {}).get("games_pitched") or 0)
        starts_a = int((a_stats or {}).get("games_pitched") or 0)
        if not (h_stats and a_stats) or starts_h < 3 or starts_a < 3:
            discarded.append({
                "game_pk":  game_pk,
                "reason":   "Datos de pitcher insuficientes (<3 aperturas).",
                "missing":  ["pitcher_data"],
                "pitcher_confirmation_source_url": statsapi_url,
            })
            continue

        # Build the scoring context.
        home_p = {
            **h_stats,
            "name": conf.get("home_pitcher_name"),
        }
        away_p = {
            **a_stats,
            "name": conf.get("away_pitcher_name"),
        }

        h_q   = pitcher_quality_score(home_p)
        a_q   = pitcher_quality_score(away_p)
        edge  = starting_pitcher_edge(home_p, away_p)
        bull  = bullpen_fatigue_score(conf.get("bullpen_usage") or {})
        park  = park_factor_analyzer(conf.get("venue"), conf.get("weather"))
        off_h = offense_vs_pitcher_type(conf.get("home_team_stats") or {},
                                        away_p.get("throws_hand", "R"))
        off_a = offense_vs_pitcher_type(conf.get("away_team_stats") or {},
                                        home_p.get("throws_hand", "R"))
        scoring_ctx = {
            "home_pitcher_quality": h_q,
            "away_pitcher_quality": a_q,
            "pitcher_edge":         edge,
            "bullpen":              bull,
            "offense_home":         off_h,
            "offense_away":         off_a,
            "park":                 park,
            "home_pitcher_stats":   home_p,
            "away_pitcher_stats":   away_p,
            "home_team":            conf.get("home_team_stats") or {},
            "away_team":            conf.get("away_team_stats") or {},
            "favorite_bullpen_era_7d": (bull or {}).get("era_7d", 0),
            "favorite_bullpen_ip_48h": ((conf.get("bullpen_usage") or {}).get("innings_last_48h") or 0),
            "favorite_one_run_win_pct": (conf.get("one_run_win_pct") or 0),
        }
        run_line   = run_line_predictor(scoring_ctx)
        over_under = over_under_predictor(scoring_ctx, conf.get("book_total"))
        nrfi       = nrfi_yrfi_analyzer(scoring_ctx)
        rescue     = mlb_alternative_rescue(scoring_ctx, run_line, over_under, nrfi)

        frag = mlb_fragility_score(scoring_ctx)
        frag_by_pk[game_pk] = frag

        # Decide bucket
        best_score = max(
            run_line.get("score", 0),
            over_under.get("score", 0),
            nrfi.get("nrfi_score", 0),
            nrfi.get("yrfi_score", 0),
        )

        # Hard guardrails (user's "lo que NO debe hacer")
        skip_over_coors = (
            (park.get("venue") == "Coors Field")
            and float(home_p.get("xera") or 9) < 3.50
            and float(away_p.get("xera") or 9) < 3.50
        )
        if skip_over_coors:
            over_under["verdict"] = "NO_BET"

        chosen_market: Optional[dict] = None
        if frag.get("score", 100) > 60:
            chosen_market = None  # fragility too high — never recommend
        elif "RUN_LINE_TRAP" not in run_line.get("tags", []) and run_line.get("score", 0) >= 72:
            chosen_market = {"market": "Run Line", "score": run_line["score"],
                             "rationale": run_line.get("explanation", "")}
        elif over_under.get("verdict") in ("OVER", "UNDER") and over_under.get("score", 0) >= 72:
            chosen_market = {"market": f"Total Runs {over_under['verdict'].title()}",
                             "score": over_under["score"],
                             "rationale": over_under.get("explanation", "")}
        elif "NRFI_SIGNAL" in nrfi.get("tags", []) and frag.get("score", 100) <= 45:
            chosen_market = {"market": "NRFI", "score": nrfi.get("nrfi_score", 0),
                             "rationale": nrfi.get("explanation", "")}

        # Emit signals (with literal source_url for transparency)
        parts = {
            "home_pitcher_quality": h_q, "away_pitcher_quality": a_q,
            "pitcher_edge": edge, "bullpen": bull, "park": park,
            "run_line": run_line, "over_under": over_under, "nrfi": nrfi,
            "fragility": frag,
            "rescued_candidates": rescue.get("candidates") or [],
        }
        signals = emit_signals(scoring_ctx, parts, source_url=statsapi_url)
        signals_by_pk[game_pk] = signals

        pick_payload = {
            "game_pk":        game_pk,
            "home_pitcher":   conf.get("home_pitcher_name"),
            "away_pitcher":   conf.get("away_pitcher_name"),
            "pitcher_confirmation_source_url": statsapi_url,
            "scores": {
                "pitcher_edge":   edge.get("score", 0),
                "run_line":       run_line.get("score", 0),
                "over_under":     over_under.get("score", 0),
                "nrfi":           nrfi.get("nrfi_score", 0),
                "yrfi":           nrfi.get("yrfi_score", 0),
            },
            "fragility":      frag,
            "expected_runs":  over_under.get("expected_runs"),
            "editorial_context_signals": signals,
            "all_components": parts,
            "rescued_candidates": rescue.get("candidates") or [],
        }

        if chosen_market and best_score >= 72:
            pick_payload["recommendation"] = chosen_market
            picks.append(pick_payload)
        elif rescue.get("candidates"):
            pick_payload["recommendation"] = rescue["candidates"][0]
            rescued.append(pick_payload)
        else:
            pick_payload["discard_reason"] = (
                "Edge insuficiente en mercado directo y sin alternativa válida."
            )
            discarded.append(pick_payload)

    pipeline_meta["picks_total"]     = len(picks)
    pipeline_meta["rescued_total"]   = len(rescued)
    pipeline_meta["discarded_total"] = len(discarded)
    pipeline_meta["finished_at"]     = datetime.now(timezone.utc).isoformat()

    return {
        "picks":            picks,
        "rescued_picks":    rescued,
        "discarded_picks":  discarded,
        "fragility_scores": {str(k): v for k, v in frag_by_pk.items()},
        "editorial_context_signals_by_game": {str(k): v for k, v in signals_by_pk.items()},
        "pipeline_meta":    pipeline_meta,
    }


def _empty_payload(pipeline_meta: dict) -> dict:
    return {
        "picks":            [],
        "rescued_picks":    [],
        "discarded_picks":  [],
        "fragility_scores": {},
        "editorial_context_signals_by_game": {},
        "pipeline_meta":    pipeline_meta,
    }


__all__ = ["analyze_mlb_day"]

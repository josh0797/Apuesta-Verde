@api.post("/picks/track")
async def track_pick(payload: TrackIn, user: dict = Depends(get_current_user)):
    # Validate sport first (best-effort — accept anything but default to football)
    sport = (payload.sport or "football").lower()
    if sport not in SUPPORTED_SPORTS:
        sport = "football"

    # ── Deterministic pick_uid ──────────────────────────────────────
    # Bug fix: previously `pick_uid = f"{run_id}-{match_id}"`, but the
    # /analysis/recalibrate endpoint mints a fresh run_id on every call.
    # The unique index `(user_id, match_id, pick_id)` therefore did NOT
    # collide and the same match got persisted with multiple pick_uids
    # → duplicated rows in the history view, distorted win-rate maths.
    # Anchoring pick_uid to (sport, date, anchor_id, market_norm, sel_norm)
    # makes it idempotent across recalibrations AND across the same game
    # being suggested with multiple markets / sides on the same day.
    now = datetime.now(timezone.utc)
    tracked_date = now.strftime("%Y-%m-%d")
    # MLB anchors prefer game_pk (stable across recalibrations); other
    # sports fall back to match_id.
    anchor_id = (
        str(payload.game_pk)
        if (sport == "baseball" and payload.game_pk)
        else str(payload.match_id)
    )
    # Slug-ify market+selection so different bets on the SAME game land
    # in different rows (e.g. Under 9.5 vs Run Line -1.5).
    _market_norm = "".join(c.lower() if c.isalnum() else "-"
                            for c in (payload.market or "m"))
    _sel_norm    = "".join(c.lower() if c.isalnum() else "-"
                            for c in (payload.selection or "s"))[:24]
    # Use match_date when known — falls back to tracked_date for legacy.
    date_part = (payload.match_date or tracked_date)
    pick_uid = f"{sport}-{date_part}-{anchor_id}-{_market_norm[:24]}-{_sel_norm}"

    # ── Build final_score block defensively ─────────────────────────
    final_score_block = None
    if payload.final_score is not None:
        fs = payload.final_score
        display = fs.display
        if display is None and fs.home is not None and fs.away is not None:
            display = f"{fs.home}-{fs.away}"
        final_score_block = {
            "home":    fs.home,
            "away":    fs.away,
            "display": display,
        }

    # ── Settled-at: set automatically when outcome leaves pending ──
    settled_at = payload.settled_at
    if settled_at is None and payload.outcome in (
        "won", "lost", "push", "void", "cancelled", "refund",
        "cashout_win", "cashout_loss",
    ):
        settled_at = now.isoformat()

    doc = {
        "user_id":           user["id"],
        "pick_id":           pick_uid,
        "pick_uid":          pick_uid,       # canonical alias for the UI
        "run_id":            payload.run_id,
        "match_id":          str(payload.match_id),
        "game_pk":           (str(payload.game_pk) if payload.game_pk else None),
        "match_label":       payload.match_label,
        "home_team":         payload.home_team,
        "away_team":         payload.away_team,
        "league":            payload.league,
        "market":            payload.market,
        "selection":         payload.selection,
        "line":              payload.line,
        "confidence_score":  payload.confidence_score,
        "outcome":           payload.outcome,
        "result":            payload.outcome,  # mirror as `result` (per spec)
        "odds":              payload.odds,
        "notes":             payload.notes,
        "sport":             sport,
        "tracked_date":      tracked_date,
        "tracked_at":        now.isoformat(),
        "match_date":        payload.match_date,
        "kickoff_iso":       payload.kickoff_iso,
        "is_live":           bool(payload.is_live) if payload.is_live is not None else False,
        "final_score":       final_score_block,
        "settled_at":        settled_at,
        "source":            payload.source or "engine",
        "pipeline_version":  payload.pipeline_version,
        # Live-entry context (Fix 5+6) — all optional.
        "entry_minute":       payload.entry_minute,
        "entry_score_home":   payload.entry_score_home,
        "entry_score_away":   payload.entry_score_away,
        "entry_score_display": (
            payload.entry_score_display
            if payload.entry_score_display is not None
            else (
                f"{payload.entry_score_home}-{payload.entry_score_away}"
                if (payload.entry_score_home is not None and payload.entry_score_away is not None)
                else None
            )
        ),
        # ── Phase 42 — Line Learning actual-bet snapshot ──
        "actual_bet": {
            "market":    payload.actual_market    or payload.market,
            "selection": payload.actual_selection or payload.selection,
            "line":      payload.actual_line if payload.actual_line is not None else payload.line,
            "odds":      payload.actual_odds      or payload.odds,
            "outcome":   payload.actual_outcome   or payload.outcome,
        },
        "engine_recommendation": {
            "market":      payload.market,
            "selection":   payload.selection,
            "line":        payload.line,
            "odds":        payload.odds,
            "projection":  payload.engine_projection,
            "outcome":     payload.outcome,
        },
        "market_type":       payload.market_type,
        "final_value":       payload.final_value,
        "engine_projection": payload.engine_projection,
    }

    # ── One-time dedup of stale rows from prior recalibrations ──────
    # Pre-fix rows have heterogeneous pick_ids (e.g. "abc123-778900"
    # then "def456-778900" on the second recalibration). Remove every
    # match-same-day row that does NOT carry the new deterministic uid
    # so the upsert below leaves a single canonical document.
    try:
        old_filter = {
            "user_id":    user["id"],
            "match_id":   str(payload.match_id),
            "sport":      sport,
            "tracked_at": {"$regex": f"^{tracked_date}"},
            "pick_id":    {"$ne": pick_uid},
        }
        stale = await db.pick_tracking.delete_many(old_filter)
        if stale.deleted_count:
            log.info(
                "pick_tracking dedup: removed %d stale row(s) for match=%s sport=%s date=%s",
                stale.deleted_count, payload.match_id, sport, tracked_date,
            )
    except Exception as _exc_dedup:
        log.warning("pick_tracking dedup failed (non-fatal): %s", _exc_dedup)

    # ── Fix 1+2: Engine vs User pick divergence analysis ─────────────
    # If the user diverged from the engine (different market/line/side),
    # auto-settle the engine's recommendation against the OFFICIAL final
    # score so we can track pure Engine Accuracy separately from User
    # Accuracy. Fail-soft: never block the track_pick response.
    try:
        from services import pick_divergence_analysis as _pda
        f5h = getattr(payload.final_score, "f5_home", None) if payload.final_score else None
        f5a = getattr(payload.final_score, "f5_away", None) if payload.final_score else None
        fh = final_score_block.get("home") if final_score_block else None
        fa = final_score_block.get("away") if final_score_block else None

        diverg = _pda.evaluate_engine_vs_user(
            engine_market    = payload.market,
            engine_selection = payload.selection,
            engine_line      = payload.line,
            user_market      = payload.actual_market,
            user_selection   = payload.actual_selection,
            user_line        = payload.actual_line,
            final_home       = fh,
            final_away       = fa,
            f5_home          = f5h,
            f5_away          = f5a,
        )
        doc["divergence"] = {
            "available":       diverg.get("available"),
            "engine_result":   diverg.get("engine_result"),
            "user_result":     diverg.get("user_result"),
            "followed_engine": diverg.get("followed_engine"),
            "delta":           diverg.get("delta"),
            "line_difference": diverg.get("line_difference"),
            "line_direction":  diverg.get("line_direction"),
            "pick_variation":  diverg.get("pick_variation"),
            "engine_pick":     diverg.get("engine_pick"),
            "user_pick":       diverg.get("user_pick"),
        }
        # Surface the engine_result at top-level so dashboards/queries
        # don't have to dig into the sub-document.
        if diverg.get("engine_result") and diverg["engine_result"] != "PENDING":
            doc["engine_result"] = diverg["engine_result"]
        if diverg.get("user_result") and diverg["user_result"] != "PENDING":
            doc["user_result"] = diverg["user_result"]
    except Exception as _exc_div:
        log.debug("pick_divergence_analysis failed (non-fatal): %s", _exc_div)

    await db.pick_tracking.update_one(
        {"user_id": user["id"], "match_id": str(payload.match_id), "pick_id": pick_uid},
        {"$set": doc},
        upsert=True,
    )

    # Fix 5+6 — mirror live picks into live_recommendation_events.
    # When `is_live=True`, we ALWAYS publish a snapshot to the event
    # timeline carrying the source flag (engine|manual). When the user
    # later updates the outcome to won/lost/push/void/refund/cancelled,
    # we settle the matching event so the timeline reflects reality.
    # Fail-soft: never block the track_pick response on history I/O.
    try:
        if doc.get("is_live"):
            from services.live_recommendation_history import (
                record_manual_live_event,
                settle_live_recommendation_event,
                COLLECTION as _LR_COLL,
            )
            source_flag = (payload.source or "engine").lower()
            if source_flag not in ("engine", "manual"):
                source_flag = "engine"
            score_label = doc.get("entry_score_display")
            entry_score = {
                "label": score_label,
                "home":  doc.get("entry_score_home"),
                "away":  doc.get("entry_score_away"),
            }
            outcome_map = {
                "won": "hit", "lost": "miss", "push": "push",
                "void": "void", "refund": "void", "cancelled": "void",
            }
            event_payload = {
                "sport":       sport,
                "match_id":    str(payload.match_id),
                "match_label": payload.match_label,
                "league":      payload.league,
                "minute":      doc.get("entry_minute"),
                "score":       entry_score,
                "recommendation": {
                    "title":              payload.market,
                    "market":             payload.market,
                    "selection":          payload.selection,
                    "confidence":         payload.confidence_score,
                    "recommended_action": "LIVE_ENTRY",
                },
                "live_context": {
                    "odds":           payload.odds,
                    "pick_uid":       pick_uid,
                    "source":         source_flag,
                    "tracked_at":     now.isoformat(),
                    "entry_minute":   doc.get("entry_minute"),
                },
                "reason":      payload.notes,
                "notes":       payload.notes,
                "outcome": {
                    "result": outcome_map.get(payload.outcome, "pending"),
                },
            }
            # Use record_manual_live_event() so the dedup window respects
            # repeated saves of the SAME (match, minute, score, market).
            evt = await record_manual_live_event(
                db, user_id=user["id"], payload=event_payload,
            )
            # If recording was skipped due to dedup but the user is settling
            # a previously-open snapshot, find the open event and settle it.
            if not evt and payload.outcome in (
                "won", "lost", "push", "void", "refund", "cancelled"
            ):
                open_evt = await db[_LR_COLL].find_one({
                    "user_id":  user["id"],
                    "sport":    sport,
                    "match_id": str(payload.match_id),
                    "status":   {"$in": ["open", "manual_recorded"]},
                }, sort=[("created_at", -1)])
                if open_evt:
                    await settle_live_recommendation_event(
                        db,
                        event_id=open_evt["event_id"],
                        result=payload.outcome,
                        settled_minute=doc.get("entry_minute"),
                        settled_score=score_label,
                        settlement_reason="user-marked-outcome",
                    )
            # Patch the freshly-recorded event with the explicit source flag
            # (record_manual_live_event always sets source="manual"; we
            # override with the actual source so the UI distinguishes
            # engine-driven vs user-chosen plays).
            if evt and evt.get("event_id"):
                try:
                    await db[_LR_COLL].update_one(
                        {"event_id": evt["event_id"]},
                        {"$set": {"source": source_flag}},
                    )
                except Exception:
                    pass
    except Exception as exc_mirror:
        log.debug("live_recommendation_events mirror failed (non-fatal): %s", exc_mirror)

    # ── Phase 42 / Line Learning Engine — Features 1-5, 9 ───────────
    # Build a learning sample from this settled pick and persist it
    # to `line_learning_samples`. Observe-only by default until the
    # cohort (sport, market_type) reaches LINE_LEARNING_MIN_SAMPLES.
    # Fail-soft: never block the track_pick response on learning I/O.
    try:
        if payload.outcome in (
            "won", "lost", "push", "void", "refund", "cancelled",
            "cashout_win", "cashout_loss",
        ):
            from services import line_learning_engine as lle

            market_type = payload.market_type or lle._infer_market_type(
                payload.market, payload.selection,
            )
            # Cohort count BEFORE this sample lands.
            try:
                cohort_count = await db.line_learning_samples.count_documents({
                    "user_id":     user["id"],
                    "sport":       sport,
                    "market_type": market_type,
                })
            except Exception:
                cohort_count = 0

            # Pull a sensible final_value when caller didn't provide one.
            fv = payload.final_value
            if fv is None and final_score_block:
                try:
                    h = float(final_score_block.get("home") or 0)
                    a = float(final_score_block.get("away") or 0)
                    fv = h + a
                except (TypeError, ValueError):
                    fv = None

            sample = lle.build_learning_sample(
                user_id=user["id"],
                match_id=str(payload.match_id),
                sport=sport,
                market_type=market_type,
                engine_market=payload.market,
                engine_selection=payload.selection,
                engine_line=payload.line,
                engine_odds=payload.odds,
                engine_projection=payload.engine_projection,
                engine_outcome=payload.outcome,
                user_market=payload.actual_market,
                user_selection=payload.actual_selection,
                user_line=payload.actual_line,
                user_odds=payload.actual_odds,
                user_outcome=payload.actual_outcome,
                final_value=fv,
                final_score=final_score_block,
                cohort_sample_count=cohort_count,
            )
            # Idempotent upsert by (user_id, match_id, market, selection).
            await db.line_learning_samples.update_one(
                {
                    "user_id":  user["id"],
                    "match_id": str(payload.match_id),
                    "engine.market":    payload.market,
                    "engine.selection": payload.selection,
                },
                {"$set": sample, "$setOnInsert": {"created_at": sample["created_at"]}},
                upsert=True,
            )
            # Stash a compact summary on the pick_tracking row so the UI
            # can render the learning verdict without an extra round-trip.
            try:
                await db.pick_tracking.update_one(
                    {"user_id": user["id"], "match_id": str(payload.match_id), "pick_id": pick_uid},
                    {"$set": {
                        "line_learning": {
                            "classification":  sample.get("classification"),
                            "reason_codes":    sample.get("reason_codes"),
                            "line_distance":   sample.get("line_distance"),
                            "summary_es":      sample.get("summary_es"),
                            "summary_en":      sample.get("summary_en"),
                            "observe_only":    sample.get("observe_only"),
                            "engine_version":  sample.get("engine_version"),
                        },
                    }},
                )
            except Exception:
                pass
    except Exception as exc_lle:
        log.debug("line_learning persist failed (non-fatal): %s", exc_lle)

    return {"ok": True, "pick_id": pick_uid, "outcome": payload.outcome, "sport": sport}


@api.get("/picks/tracked")
async def list_tracked(
    sport: Optional[str] = None,
    user: dict = Depends(get_current_user),
    limit: int = 200,
):
    """Return the user's tracked picks with optional sport filter.

    Query params
    ------------
    sport : "baseball" | "football" | "basketball" | "mlb" | "nba" | "soccer"
        Aliases supported:
          - baseball ⇔ mlb
          - football ⇔ soccer
          - basketball ⇔ nba
        When omitted, returns ALL sports (legacy behaviour).

    The response is server-side deduplicated by the canonical pick_uid
    AND a defensive ``(sport, anchor_id, market, selection, line)`` key
    so legacy rows generated with the old non-unique pick_uid don't
    repeat in the History UI.
    """
    base = {"user_id": user["id"]}
    # ── Sport filter (with alias resolution) ────────────────────
    if sport:
        norm = sport.lower().strip()
        ALIASES = {
            "baseball":   ["baseball", "mlb"],
            "mlb":        ["baseball", "mlb"],
            "football":   ["football", "soccer"],
            "soccer":     ["football", "soccer"],
            "basketball": ["basketball", "nba"],
            "nba":        ["basketball", "nba"],
        }
        sport_keys = ALIASES.get(norm, [norm])
        if "football" in sport_keys:
            # Legacy rows without a sport field default to football.
            base["$or"] = [
                {"sport": {"$in": sport_keys}},
                {"sport": {"$exists": False}},
            ]
        else:
            base["sport"] = {"$in": sport_keys}

    docs = (await db.pick_tracking
            .find(base)
            .sort("tracked_at", -1)
            .limit(limit * 3)   # over-fetch to absorb dedup losses
            .to_list(length=limit * 3))
    cleaned = _clean_list(docs)

    # ── Defensive deduplication ────────────────────────────────────
    # Keep the most-recent row per canonical key. Two keys are checked:
    #   1) pick_uid (when present) — exact idempotency.
    #   2) (sport, anchor_id, market, selection, line) — recovers old
    #      rows that never received a stable pick_uid.
    seen_uids:  set[str] = set()
    seen_keys:  set[tuple] = set()
    deduped: list[dict] = []
    for row in cleaned:
        uid = row.get("pick_uid") or row.get("pick_id")
        anchor = row.get("game_pk") or row.get("match_id")
        key = (
            (row.get("sport") or "football"),
            str(anchor or ""),
            (row.get("market") or "").lower(),
            (row.get("selection") or "").lower(),
            row.get("line"),
        )
        if uid and uid in seen_uids:
            continue
        if key in seen_keys:
            continue
        if uid:
            seen_uids.add(uid)
        seen_keys.add(key)
        deduped.append(row)
        if len(deduped) >= limit:
            break
    return {"count": len(deduped), "items": deduped}



@api.get("/matches/{match_id}")
async def match_detail(match_id: str, user: dict = Depends(get_current_user)):
    """Get full detail of a single match (3 layers).

    Robust to ID-shape mismatch: MLB matches are stored with a `str`
    `match_id` (the gamePk as text) while football/basketball matches
    historically used `int`. We probe both shapes so the user never
    lands on a 404 just because of casting.

    For baseball matches we also opportunistically hydrate live state
    from MLB Stats API so the detail view always shows the current
    inning + score, not a stale snapshot from the periodic ingestion.
    """
    candidates: list = []
    # Try the raw string first (preserves leading zeros / hashes).
    candidates.append(match_id)
    # Then try the int form when parseable.
    try:
        mid_int = int(match_id)
        if mid_int not in candidates:
            candidates.append(mid_int)
    except (ValueError, TypeError):
        pass

    doc = await db.matches.find_one({"match_id": {"$in": candidates}})
    # Last-resort: maybe the match was archived to `archived_live_matches`
    # after the live feed closed it. Look there too so the detail page
    # works for recently-finished games.
    if not doc:
        try:
            doc = await db.archived_live_matches.find_one(
                {"match_id": {"$in": candidates}}
            )
        except Exception:
            doc = None
    if not doc:
        raise HTTPException(status_code=404, detail="match not found")

    # ── Opportunistic live hydration (MLB only) ───────────────────────
    # Stale `is_live=False` is the most common complaint: the
    # ingestion sweep runs every 15-30s but the user can click into
    # the detail in between. We hit the linescore endpoint once
    # (≤6s timeout) and merge the result so the user sees a fresh
    # scoreboard. The persistence inside fetch_and_persist_live_state
    # also benefits the next call.
    try:
        if (doc.get("sport") == "baseball"
                and (doc.get("status") or "").lower() != "final"
                and "final" not in (doc.get("status") or "").lower()):
            from services.mlb_live_state import fetch_and_persist_live_state
            live = await fetch_and_persist_live_state(db, doc.get("match_id"))
            if live.get("game_pk") is not None:
                # Merge into the doc so the response is internally consistent
                # without a second round-trip to Mongo.
                doc["is_live"]    = live["is_live"]
                doc["status"]     = live["status"] or doc.get("status")
                # BUGFIX MLB-2b — surface the canonical game_pk at the doc
                # root so the pregame-pick lookup below can match picks
                # that store the identifier as `game_pk` rather than
                # `match_id`. Idempotent overwrite (always the same value).
                doc["game_pk"]    = live["game_pk"]
                doc["live_stats"] = {
                    **(doc.get("live_stats") or {}),
                    "score":           live["score"],
                    "inning":          live.get("inning"),
                    "outs":            live.get("outs"),
                    "balls":           live.get("balls"),
                    "strikes":         live.get("strikes"),
                    "runners_on":      live.get("runners_on"),
                    "current_batter":  live.get("current_batter"),
                    "current_pitcher": live.get("current_pitcher"),
                    "abstract_state":  live.get("abstract_state"),
                    "detailed_state":  live.get("detailed_state"),
                    "box_score":       live.get("box_score"),
                    "fetched_at":      live["fetched_at"],
                }
                doc["_live_state"] = live["state"]   # for the UI banner.
    except Exception as exc:
        log.debug("opportunistic MLB live hydrate failed: %s", exc)

    # Fetch full odds history using whichever ID variant matches docs.
    snapshots: list = []
    try:
        snapshots = await db.odds_snapshots.find(
            {"match_id": {"$in": candidates}}
        ).sort("snapshot_at", -1).limit(10).to_list(length=10)
    except Exception:
        snapshots = []
    doc["odds_history"] = _clean_list(snapshots)

    # ── Live ↔ Pregame comparison (Layer A + B) ───────────────────────
    # We enrich the doc with a `script_comparison` object whenever we
    # have BOTH a live snapshot AND a pregame pick for this match in the
    # user's latest pick_run. This is what the UI consumes to decide
    # whether to render the pick as actionable or move it to the
    # "Pick pregame ya cumplido/no accionable" section.
    try:
        pregame_pick = None
        # Look only in the user's latest pick_run to keep the join cheap.
        last_run = await db.picks.find_one(
            {"user_id": user["id"], "sport": doc.get("sport")},
            sort=[("generated_at", -1)],
            projection={"payload": 1},
        )
        if last_run and last_run.get("payload"):
            buckets = (
                (last_run["payload"].get("picks")          or []) +
                (last_run["payload"].get("rescued_picks")  or []) +
                (last_run["payload"].get("structural_lean_requires_odds") or []) +
                (last_run["payload"].get("watchlist_manual_odds") or [])
            )
            # BUGFIX MLB-2b — build a set of every possible identifier this
            # doc can be referenced by. For baseball, picks may use
            # `game_pk` (from MLB Stats API) while the doc's `match_id`
            # comes from a different ingestion path (rare but observed).
            # We accept either side so the lookup never silently misses.
            doc_id_candidates: set[str] = set()
            mid = doc.get("match_id")
            if mid is not None:
                doc_id_candidates.add(str(mid))
                try:
                    doc_id_candidates.add(str(int(mid)))
                except (TypeError, ValueError):
                    pass
            if doc.get("sport") == "baseball":
                # Also accept by gamePk (doc-level or nested under live_stats)
                gp = doc.get("game_pk")
                if gp is None:
                    gp = (doc.get("live_stats") or {}).get("game_pk")
                if gp is not None:
                    doc_id_candidates.add(str(gp))
                    try:
                        doc_id_candidates.add(str(int(gp)))
                    except (TypeError, ValueError):
                        pass
            for p in buckets:
                pick_id_candidates: set[str] = set()
                for fld in ("match_id", "game_pk"):
                    v = p.get(fld)
                    if v is not None:
                        pick_id_candidates.add(str(v))
                        try:
                            pick_id_candidates.add(str(int(v)))
                        except (TypeError, ValueError):
                            pass
                if pick_id_candidates & doc_id_candidates:
                    pregame_pick = p
                    break
        live_state_for_cmp = doc.get("live_stats") or {}
        # Build a compatible shape for the comparison layer (it expects
        # the fetcher's payload, which uses `score` / `inning` / `state`).
        live_shape = {
            "score":  live_state_for_cmp.get("score"),
            "inning": live_state_for_cmp.get("inning"),
            "state":  doc.get("_live_state")
                      or ("live-data-ready" if doc.get("is_live") else "no-live-data"),
            "is_live": bool(doc.get("is_live")),
            "status":  doc.get("status"),
        }
        from services.live_pre_match_comparison import compare_pregame_vs_live
        doc["script_comparison"] = compare_pregame_vs_live(
            pregame_pick=pregame_pick,
            live_state=live_shape,
            sport=doc.get("sport") or "baseball",
        )
    except Exception as exc:
        log.debug("script_comparison enrichment failed (non-fatal): %s", exc)
        doc["script_comparison"] = None

    return _clean(doc)



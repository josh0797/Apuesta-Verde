@api.post("/mlb/picks/{pick_id}/settle")
async def settle_mlb_pick(pick_id: str, payload: MLBSettleIn,
                          user: dict = Depends(get_current_user)):
    """Settle an MLB pick. Writes margin/totalRuns/runLineCovered/overHit to
    `pick_tracking` AND a full v2 snapshot to `mlb_pick_feedback`. Triggers
    automatic recalibration of the engine weights every FEEDBACK_BATCH_SIZE
    settled picks (default 50).
    """
    if pick_id != payload.pick_id:
        return JSONResponse(status_code=400,
                            content={"detail": "pick_id path mismatch"})
    try:
        from services.mlb_feedback_loop import record_mlb_pick_outcome
        result = await record_mlb_pick_outcome(
            db,
            pick_id=payload.pick_id,
            run_id=payload.run_id,
            match_id=str(payload.match_id),
            user_id=user["id"],
            outcome=payload.outcome,
            final_home_runs=payload.final_home_runs,
            final_away_runs=payload.final_away_runs,
            v2_snapshot=payload.v2_snapshot,
            pick_doc=payload.pick_doc,
        )

        # ── History UI mirror — final_score + settled_at ─────────
        # Even though `record_mlb_pick_outcome` already updates the
        # margin/totalRuns metrics, the History page reads ``final_score``
        # and ``settled_at`` at the row level. Mirror them here so the
        # UI always shows a readable "5-3" badge per settled pick.
        try:
            if payload.final_home_runs is not None and payload.final_away_runs is not None:
                final_score_block = {
                    "home":    int(payload.final_home_runs),
                    "away":    int(payload.final_away_runs),
                    "display": f"{int(payload.final_home_runs)}-{int(payload.final_away_runs)}",
                }
            else:
                final_score_block = None
            settled_at_iso = datetime.now(timezone.utc).isoformat()
            mirror_set: dict = {
                "result":     payload.outcome,
                "settled_at": settled_at_iso,
            }
            if final_score_block:
                mirror_set["final_score"] = final_score_block
            # Best-effort: also surface the canonical teams when the
            # caller passed them in pick_doc.
            if isinstance(payload.pick_doc, dict):
                if payload.pick_doc.get("home_team"):
                    mirror_set["home_team"] = payload.pick_doc["home_team"]
                if payload.pick_doc.get("away_team"):
                    mirror_set["away_team"] = payload.pick_doc["away_team"]
                if payload.pick_doc.get("match_date"):
                    mirror_set["match_date"] = payload.pick_doc["match_date"]
                if payload.pick_doc.get("game_pk"):
                    mirror_set["game_pk"] = str(payload.pick_doc["game_pk"])
            await db.pick_tracking.update_one(
                {"user_id": user["id"], "pick_id": payload.pick_id},
                {"$set": mirror_set},
            )
        except Exception as _mirror_exc:
            log.warning(
                "History mirror failed for pick %s: %s",
                payload.pick_id, _mirror_exc,
            )
        # F6B — Persist a script-break event for the future learning loop.
        # Fail-soft: a storage error must not block the settle response.
        try:
            from services.mlb_script_breaks_storage import store_script_break_event
            sb_result = await store_script_break_event(
                db,
                pick_id=payload.pick_id,
                run_id=payload.run_id,
                match_id=str(payload.match_id),
                user_id=user["id"],
                outcome=payload.outcome,
                final_home_runs=payload.final_home_runs,
                final_away_runs=payload.final_away_runs,
                pick_doc=payload.pick_doc,
                v2_snapshot=payload.v2_snapshot,
            )
            result["script_break"] = sb_result
        except Exception as sb_exc:
            log.warning("F6B script-break storage failed for pick %s: %s",
                        payload.pick_id, sb_exc)
            result["script_break"] = {"ok": False, "error": str(sb_exc)}

        # F6C — Cerrar el feedback loop de mlb_run_evaluations.
        # Busca el evaluation_id en pick_doc (pregame) o v2_snapshot (live)
        # y llama a update_run_evaluation_result para resolver el documento
        # de "pending" a won/lost/push. Sin esto el reference_profile_tag
        # nunca se activa y el feedback loop explosivo no aprende.
        # Fail-soft: nunca bloquea la respuesta del settle.
        try:
            if payload.outcome in ("won", "lost", "push") \
                    and payload.final_home_runs is not None \
                    and payload.final_away_runs is not None:

                from services.mlb_run_storage import (
                    update_run_evaluation_result,
                    query_run_evaluations,
                )

                _pd  = payload.pick_doc  or {}
                _v2  = payload.v2_snapshot or {}

                # Resolver evaluation_id — pick_doc es la fuente principal
                # (el orquestador lo escribe como pick_payload["mlb_run_evaluation_id"]).
                # v2_snapshot["explosive_v2"] es el fallback para picks live.
                _eval_id = (
                    _pd.get("mlb_run_evaluation_id")
                    or (_v2.get("explosive_v2") or {}).get("mlb_run_evaluation_id")
                    or _pd.get("run_evaluation_id")
                )

                # Fallback: si no hay id explícito, buscar el documento
                # pending más reciente para este match_id.
                if not _eval_id:
                    _candidates = await query_run_evaluations(
                        db,
                        user_id="_slate",
                        match_id=str(payload.match_id),
                        result="pending",  # solo los sin resolver
                        limit=1,
                    )
                    if not _candidates:
                        # Intentar también con el user_id real del settle
                        _candidates = await query_run_evaluations(
                            db,
                            user_id=user["id"],
                            match_id=str(payload.match_id),
                            result="pending",
                            limit=1,
                        )
                    if _candidates:
                        _eval_id = _candidates[0].get("id")

                if _eval_id:
                    # Derivar miss_type a partir del outcome y el mercado.
                    _miss_type = None
                    if payload.outcome == "lost":
                        _mkt = (
                            _pd.get("market")
                            or (_pd.get("recommendation") or {}).get("market")
                            or ""
                        ).lower()
                        if "under" in _mkt:
                            _miss_type = "OVER_BEAT_UNDER"
                        elif "over" in _mkt:
                            _miss_type = "UNDER_BEAT_OVER"
                    elif payload.outcome == "push":
                        _miss_type = "PUSH"

                    _updated = await update_run_evaluation_result(
                        db,
                        evaluation_id=_eval_id,
                        final_runs_home=payload.final_home_runs,
                        final_runs_away=payload.final_away_runs,
                        result=payload.outcome,
                        miss_type=_miss_type,
                    )
                    result["run_evaluation_updated"] = _updated
                    result["run_evaluation_id"]      = _eval_id
                    if _updated:
                        log.info(
                            "F6C run_evaluation resolved pick=%s eval=%s outcome=%s miss=%s",
                            payload.pick_id, _eval_id[:8], payload.outcome, _miss_type,
                        )
                    else:
                        log.warning(
                            "F6C run_evaluation not found or failed pick=%s eval=%s",
                            payload.pick_id, _eval_id,
                        )
                else:
                    result["run_evaluation_updated"] = False
                    result["run_evaluation_id"]      = None
                    log.debug(
                        "F6C no evaluation_id found for pick=%s match=%s — skipping",
                        payload.pick_id, payload.match_id,
                    )
        except Exception as _exc_f6c:
            log.warning("F6C run_evaluation update failed (non-fatal) pick=%s: %s",
                        payload.pick_id, _exc_f6c)
            result["run_evaluation_updated"] = False

        return {"ok": True, **result}
    except Exception as exc:
        log.exception("mlb settle failed: %s", exc)
        return JSONResponse(status_code=500,
                            content={"ok": False, "detail": str(exc)})


